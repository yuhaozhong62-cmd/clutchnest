function streamerCrosshair(input: StreamerCrosshairInput): CrosshairVersion {
  return {
    id: input.id,
    slug: input.id,
    titleZh: input.titleZh,
    titleEn: input.titleEn,
    code: input.code,
    versionStatus: "primary",
    verificationStatus: input.verificationStatus ?? "likely",
    normalizedSettings: input.settings,
    color: input.color,
    summaryZh: input.summaryZh,
    summaryEn: input.summaryEn,
    styleTags: input.tags,
    recommendedFor: input.recommendedFor,
    strengths: input.strengths,
    weaknesses: input.weaknesses,
    lastVerifiedAt: VERIFIED_AT,
    verificationNotes: "公开设置可能随时变化；此处保存的是 ClutchNest 在核验日能够合理确认的版本。",
    sources: input.sources,
    haoTestStatus: "untested",
    contentType: "streamer-reference",
    createdBy: "ClutchNest Research",
    status: "published",
    nextReviewAt: NEXT_REVIEW_AT,
    analysis: input.analysis
  };
}

